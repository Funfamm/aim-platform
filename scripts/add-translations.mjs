// ── Merge and write ───────────────────────────────────────────────────────────
const locales = ['en','fr','es','pt','de','ar','hi','ja','ko','zh','ru']

for (const locale of locales) {
  const file = join(messagesDir, `${locale}.json`)
  const data = JSON.parse(readFileSync(file, 'utf8'))

  // 1. Patch eventRoom namespace
  if (eventRoomPatch[locale]) {
    data.eventRoom = { ...data.eventRoom, ...eventRoomPatch[locale] }
  }

  // 2. Set watchParty namespace (full add — was missing entirely)
  if (watchPartyNS[locale]) {
    data.watchParty = { ...(data.watchParty ?? {}), ...watchPartyNS[locale] }
  }

  // Write back with 2-space indentation, UTF-8 NoBOM (Node default)
  writeFileSync(file, JSON.stringify(data, null, 2) + '\n', { encoding: 'utf8' })
  console.log(`✅ ${locale}.json updated`)
}

console.log('\n✅ All locale files updated.')
