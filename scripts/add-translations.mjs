const locales = Object.keys(translations);

for (const loc of locales) {
  const raw = readFileSync(`./src/messages/${loc}.json`);
  const hasBom = raw[0] === 0xEF && raw[1] === 0xBB && raw[2] === 0xBF;
  const str = hasBom ? raw.slice(3).toString('utf8') : raw.toString('utf8');
  const msg = JSON.parse(str);

  msg.applicationSuccess = translations[loc].applicationSuccess;
  msg.voiceRecorder = translations[loc].voiceRecorder;

  writeFileSync(`./src/messages/${loc}.json`, JSON.stringify(msg, null, 2), 'utf8');
  console.log(`✅ ${loc}: applicationSuccess + voiceRecorder written. Sections: ${Object.keys(msg).join(', ')}`);
}

console.log('\nAll done.');
