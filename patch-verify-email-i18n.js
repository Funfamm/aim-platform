let patched = 0
for (const [locale, keys] of Object.entries(translations)) {
  const filePath = path.join(MESSAGES_DIR, `${locale}.json`)
  if (!fs.existsSync(filePath)) { console.warn(`Missing: ${locale}.json`); continue }

  const content = fs.readFileSync(filePath, 'utf8')
  const json = JSON.parse(content)

  if (json.verifyEmail) { console.log(`${locale}.json already has verifyEmail — skipping`); continue }

  json.verifyEmail = keys
  fs.writeFileSync(filePath, JSON.stringify(json, null, 2), { encoding: 'utf8' })
  console.log(`✅ Patched ${locale}.json`)
  patched++
}
console.log(`\nDone — ${patched} files updated.`)
